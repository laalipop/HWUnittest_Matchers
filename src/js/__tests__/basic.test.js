import sortByHealth from '../basic';

test('test sortByHealth', () => {
  const list = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];

  const listTwo = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];

  expect(sortByHealth(list)).toEqual(listTwo);
});
